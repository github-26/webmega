<h1>Apa itu CodeIgniter</h1>
    <p>Codeigniter adalah satu dari sekian banyak framework yang biasa digunakan oleh para developer PHP. Jenis framework ini bisa dibilang paling banyak digunakan di antara jenis framework lainnya, karena mampu mempercepat pengembangan web berbasis PHP berdasarkan package serta array library yang lebih luas.

Selain itu, Framework codeigniter juga terkenal ringan karena sistem utama framework ini hanya membutuhkan beberapa library kecil sehingga bisa menghemat resource website.

Alhasil, Anda bisa lebih leluasa dalam mengembangkan website dengan kualitas terbaik tanpa harus khawatir bakal memakan resource besar pada hosting yang Anda gunakan.</p>
    <h2>Konfigurasi Minim (Nearly zero configuration)</h2>
    <p>Seperti yang telah disampaikan pada paragraf sebelumnya dengan framework Codeigniter para web developer bakal lebih mudah melakukan konfigurasi file karena sistemnya yang memang sederhana.

Bayangkan saja, ketika menggunakan Codeigniter Anda hanya perlu melakukan perubahan minor pada bagian file config jika ingin melakukan konfigurasi.

Terlepas dari itu, Anda tentu masih diberikan keleluasaan dalam proses penyesuaian database dan routing dengan mengubah beberapa file konfigurasi seperti autoload.php serta database.php.</p>
<h2>Memiliki Performa Cepat</h2>
<p>Tahukah Anda? framework berbasis PHP ini diklaim memiliki performa yang sangat cepat ketika dibandingkan dengan framework-framework lain seperti Symfony, Cake, Yii, dan Laravel.<br> Hal ini disebabkan karena framework Codeigniter tidak menggunakan tmplate engine dan ORM yang justru berpotensi memperlambat proses load website.</p>
